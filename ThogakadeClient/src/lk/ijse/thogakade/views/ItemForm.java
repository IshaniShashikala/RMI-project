    private void loadTable() {
        try {
            ArrayList<ItemDTO> alitem= ctrlItem.getAll();
            DefaultTableModel dtm = (DefaultTableModel)tableItem.getModel();
            dtm.setRowCount(0);
            for (ItemDTO item : alitem) {
                Object[] rowData = {item.getCode(),item.getDescription(),item.getQtyOnHand(),item.getUnitPrice() };
                dtm.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        
   }
