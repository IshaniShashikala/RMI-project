    private void tableItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableItemMouseClicked
        if (evt.getClickCount()==2) {
            try {
                ItemDTO resDTO = ctrlItem.get(tableItem.getValueAt(tableItem.getSelectedRow(), 0).toString());
                if (ctrlItem.reserve(resDTO.getCode())) {
                    txtID.setText(resDTO.getCode());
                    txtDescription.setText(resDTO.getDescription());
                    txtQtyOnHand.setText(resDTO.getQtyOnHand()+ "");
                    txtPrice.setText(resDTO.getUnitPrice() + "");
                    
                }else{
                    JOptionPane.showMessageDialog(this, "Failed...!");
                }
            } catch (Exception ex) {
                Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tableItemMouseClicked

    private void PrintButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrintButtonActionPerformed
       
        try {
            JasperReport compiledReport=(JasperReport) JRLoader.loadObject(ItemForm.class.getResourceAsStream("/lk/ijse/thogakade/reports/item_report.jasper"));
            JasperPrint filledReport = JasperFillManager.fillReport(compiledReport, new HashMap<String,Object>(),new JRTableModelDataSource(tableItem.getModel()));
            JasperReportViewer myViewer = new JasperReportViewer(filledReport);
            
            myViewer.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_PrintButtonActionPerformed

