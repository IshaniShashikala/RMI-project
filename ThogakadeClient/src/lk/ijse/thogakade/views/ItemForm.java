    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtID.getText().trim().isEmpty()|| txtDescription.getText().trim().isEmpty()||
                txtQtyOnHand.getText().trim().isEmpty()||txtPrice.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter valid data");
            return;
        }
        
       
        try {
            ItemDTO item = new ItemDTO(txtID.getText(), txtDescription.getText(),
                    Double.parseDouble(txtPrice.getText()), Integer.parseInt(txtQtyOnHand.getText()));

            boolean result = ctrlItem.save(item);
            if (result) {
                loadTable();
                ctrlItem.release(txtID.getText());
                clearAll();
                JOptionPane.showMessageDialog(this, "Item has been successfully added");

            } else {
                JOptionPane.showMessageDialog(this, "Failed...!");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

